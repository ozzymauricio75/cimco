class Arreglo {

	/*** Generar lista numerica***/
	public static function generarListaNumerica($inicio, $fin, $incremento = 1, $longitud = 0) {
		for ($i = $inicio; $i<= $fin; $i+= $incremento){
			$n = $i;

			if ($longitud){
				$n = str_pad($i, 2, "0", STR_PAD_LEFT);
			}
			
			$respuesta[$n] = $n;
		}
		return $respuesta;
	}

}
